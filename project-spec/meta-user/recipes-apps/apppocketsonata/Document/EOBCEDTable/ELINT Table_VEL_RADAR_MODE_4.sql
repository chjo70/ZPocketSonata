   PRI_PATTERN_PERIOD_MIN,
   PRI_PATTERN_PERIOD_MAX,
   PRI_PPG_MIN,
   PRI_PPG_MAX,
   PRI_RANGE_MIN,
   PRI_RANGE_MAX,
   PRI_RATE_MIN,
   PRI_RATE_MAX,
   PRI_SUBFRAME_PERIOD_MIN,
   PRI_SUBFRAME_PERIOD_MAX,
   PRI_TYPICAL_MIN,
   PRI_TYPICAL_MAX,
   PRI_TYPE,
      
   PD_CONTINUITY,
   PD_TYPICAL_MIN,
   PD_TYPICAL_MAX,
   PD_NUM_ELEMENTS,
   PD_NUM_POSITIONS,
   PD_PATTERN,
   PD_PATTERN_PERIOD_MIN,
   PD_PATTERN_PERIOD_MAX,
