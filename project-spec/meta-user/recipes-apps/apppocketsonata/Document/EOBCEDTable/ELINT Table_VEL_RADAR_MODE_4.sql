   RF_LEGACY_TYPE,
   PRI_LEGACY_TYPE,
   SCAN_PRIMARY_TYPE,
   ASSOC_INDEX,
   MODULATION_CODE,
   PRIORITY,
   
   MANUAL_REVISION,
   ACCEPTED,   
   
   RADAR_INDEX,
   MODE_CODE,

   ELNOT,
   NICKNAME,
   RADAR_PRIORITY,
   TIME_INACTIVATED,      
   
   THREAT_INDEX,
   DEVICE_INDEX,
   DEVICE_ELNOT
)
AS
   SELECT A.RADAR_MODE_INDEX,
          A.FUNCTION_CODE,
          A.SIGNAL_TYPE,
          A.POLARIZATION,
          A.PLATFORM,
          A.RF_TYPICAL_MIN,
          A.RF_TYPICAL_MAX,
          A.RF_CONTINUITY,
          A.RF_PATTERN,
          A.RF_NUM_ELEMENTS,
          A.RF_NUM_POSITIONS,
